int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if ((str[i] <= 'Z' && str[i] >= 'A') \
				|| (str[i] <= 'z' && str[i] >= 'a'))
		{
			i++;
		}
		else
			return (0);
	}
	return (1);
}
